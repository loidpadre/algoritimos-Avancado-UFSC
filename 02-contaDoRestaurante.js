// exercicio 2 elaborar um programa que leia o valor de um jantar,
//  calcule  e informe o valor da taxa do garçom (10%) e o valor total a ser pago
let valor_jantar = 80;
let taxa_garcon = (10 / 100) * valor_jantar;
let total_pagar = valor_jantar + taxa_garcon; 
console.log(`Sr. a taxa do garçom é de 10%, que pelo valor do jantar ${valor_jantar} R$, fica ${taxa_garcon} R$, sendo assim o valor total é: ${total_pagar} R$`)

